document.getElementById('contactForm').addEventListener('submit', function(event) {
    event.preventDefault();

    // Coletar os dados do formulário
    const formData = new FormData(this);
    const data = {};
    formData.forEach((value, key) => {
        data[key] = value;
    });

    // Fazer a requisição POST com os dados em formato JSON
    fetch('http://127.0.0.1:5500/startbootstrap-freelancer-gh-pages/index.html', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify(data)
    })
    .then(response => response.json())
    .then(result => {
        console.log('Sucesso:', result);
        // Aqui você pode adicionar lógica adicional, como exibir uma mensagem de sucesso para o usuário
    })
    .catch(error => {
        console.error('Erro:', error);
        // Aqui você pode adicionar lógica adicional, como exibir uma mensagem de erro para o usuário
    });
});